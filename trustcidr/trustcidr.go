// Package trustcidr resolves a TRUST_PROXY spec — "true" / "false" / a
// comma-separated list of CIDRs and named groups (cloudflare/google/bunny) —
// into a parapet trust Conditional. It is shared by the in-cluster controller
// and the out-of-cluster edge so both decide whether to honor inbound
// X-Forwarded-* headers from the same set of front proxies. See SPEC.md
// (TRUST_PROXY) and EDGE.md.
package trustcidr

import (
	"strings"

	"github.com/moonrhythm/parapet"
)

// Parse turns a TRUST_PROXY spec into a parapet trust Conditional:
//
//	"true"        trust every remote (inbound X-Forwarded-* honored unconditionally)
//	"" / "false"  nil — distrust all (parapet overwrites X-Forwarded-* with the peer)
//	otherwise     a comma-separated list of CIDRs and/or named groups
//	              (cloudflare/google/bunny), trusted by source IP
//
// A nil return means "trust nothing", which a parapet.Server treats as distrust
// (the edge's default: first hop, no upstream trusted).
func Parse(spec string) parapet.Conditional {
	switch spec {
	case "true":
		return parapet.Trusted()
	case "false", "":
		return nil
	default:
		var list []string
		for _, x := range strings.Split(spec, ",") {
			x = strings.TrimSpace(x)
			if x == "" {
				continue
			}
			if t := Predefined[x]; len(t) > 0 {
				list = append(list, t...)
			} else {
				list = append(list, x)
			}
		}
		return parapet.TrustCIDRs(list)
	}
}

